    private void buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar1ActionPerformed
        Usuario u = controlador.findByCedula(txtCedulaB.getText());
        if (u != null) {
            txtNombreB.setText(u.getNombre());
            txtApellidoB.setText(u.getApellido());
            txtTelefonoB.setText(u.getTelefono());
            txtDirecionB.setText(u.getDireccion());
            txtContraB.setText(u.getContracenia());
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no econtrado");
            txtNombreB.setText("");
            txtApellidoB.setText("");
            txtTelefonoB.setText("");
            txtDirecionB.setText("");
            txtContraB.setText("");
        }
    }//GEN-LAST:event_buscar1ActionPerformed


