    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean bander = true;
        if (0 >= txtCedula.getText().length()) {
            bander = false;
        }
        if (0 >= txtNombre.getText().length()) {
            bander = false;
        }
        if (0 >= txtApellido.getText().length()) {
            bander = false;
        }
        if (0 >= txtTelefono.getText().length()) {
            bander = false;
        }
        if (0 >= txtDirecion.getText().length()) {
            bander = false;
        }
        if (0 >= txtContra.getText().length()) {
            bander = false;
        }
        if (bander) {
            Usuario u = new Usuario(0, txtCedula.getText(), txtNombre.getText(), txtApellido.getText(), txtTelefono.getText(), txtDirecion.getText(), txtContra.getText(), "NO", "Sin especificar");
            try {
                if(controlador.findByCedula(txtCedula.getText()) == null){
                controlador.createUsuario(u);
                txtCedula.setText("");
                txtNombre.setText("");
                txtApellido.setText("");
                txtTelefono.setText("");
                txtDirecion.setText("");
                txtContra.setText("");    
                JOptionPane.showMessageDialog(this, "Cliente Ingresado");  
                }else{
                    JOptionPane.showMessageDialog(this, "Ya existe un usuario con esa cedula");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al guardar en la base de datos");
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Exiten errores en los campos!! \nVerfique los datos ingresados", "USUARIO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

