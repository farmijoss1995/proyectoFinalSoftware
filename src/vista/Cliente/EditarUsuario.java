    private void buscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar1ActionPerformed
        Usuario u = controlador.findByCedula(txtCedulaB.getText());
        if (u != null) {
            codAux = u.getIdUsuario();
            prestamo = u.getPrestamo();
            txtCedula.setText(u.getCedula());
            txtNombre.setText(u.getNombre());
            txtApellido.setText(u.getApellido());
            txtTelefono.setText(u.getTelefono());
            txtDirecion.setText(u.getDireccion());
            txtContra.setText(u.getContracenia());
            btnActualizar.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Usuario no econtrado");
            txtNombre.setText("");
            txtApellido.setText("");
            txtTelefono.setText("");
            txtDirecion.setText("");
            txtContra.setText("");
            codAux = 0;
            btnActualizar.setEnabled(false);
        }
    }//GEN-LAST:event_buscar1ActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        boolean bander = true;
        if (0 >= txtCedula.getText().length()) {
            bander = false;
        }
        if (0 >= txtNombre.getText().length()) {
            bander = false;
        }
        if (0 >= txtApellido.getText().length()) {
            bander = false;
        }
        if (0 >= txtTelefono.getText().length()) {
            bander = false;
        }
        if (0 >= txtDirecion.getText().length()) {
            bander = false;
        }
        if (0 >= txtContra.getText().length()) {
            bander = false;
        }
        if (bander) {
            Usuario u = new Usuario(codAux, txtCedula.getText(), txtNombre.getText(), txtApellido.getText(), txtTelefono.getText(), txtDirecion.getText(), txtContra.getText(), prestamo, motivo);
            try {
                controlador.edit(u);
                JOptionPane.showMessageDialog(this, "Cliente Actualizado");
                txtCedula.setText("");
                txtNombre.setText("");
                txtApellido.setText("");
                txtTelefono.setText("");
                txtDirecion.setText("");
                txtContra.setText("");
                btnActualizar.setEnabled(false);
                codAux = 0;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al ACTUALIZAR");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Exiten errores en los campos!! \nVerfique los datos ingresados", "USUARIO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

