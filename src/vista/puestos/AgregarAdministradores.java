    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        if (evt.getClickCount() == 2) {
            int row = tabla.getSelectedRow();
            ControladorUsuario con = new ControladorUsuario();
            Usuario u = con.findByID((int) tabla.getValueAt(row, 0));

            int conf = JOptionPane.showConfirmDialog(this, "DESEA DAR PERMISOS DE ADMINISTRADOR  AL USUARIO: \n" + u.getNombre() + " " + u.getApellido() + " \n CON CEDULA: "
                    + u.getCedula());

            if (conf == JOptionPane.OK_OPTION) {
                ControladorAdministrador cont = new ControladorAdministrador();
                Date now = new Date(System.currentTimeMillis());
                Date d = new java.util.Date();
                Date date2 = new java.sql.Date(d.getTime());
                Administrador s = new Administrador(0, date2, u);
                cont.Crear(s);
            }

            limpiarTabla();
            llenarUsuariosTabla();
            llenarUsuariosAdmin();
        }
    }//GEN-LAST:event_tablaMouseClicked


