    public void llenarUsuariosTabla() {
        controlador = new ControladorUsuario();
        List<Usuario> clientes = controlador.findAdmin();
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        int index = 0;
        for (Usuario u : clientes) {
            model.insertRow(index, new Object[]{u.getIdUsuario(), u.getCedula(), u.getNombre() + " " + u.getApellido(), u.getDireccion(), u.getTelefono()});
            index++;
        }

    }

    public void llenarUsuariosAdmin() {
        ControladorAdministrador cont = new ControladorAdministrador();
        List<Administrador> clientes = cont.findAll();
        DefaultTableModel model = (DefaultTableModel) tabla1.getModel();
        int index = 0;
        for (Administrador x : clientes) {
            Usuario u = x.getIdUsuario();
            model.insertRow(index, new Object[]{u.getIdUsuario(), u.getCedula(), u.getNombre() + " " + u.getApellido(), u.getDireccion(), u.getTelefono()});
            index++;
        }

    }

    public void limpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        modelo = (DefaultTableModel) tabla1.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }

